@implementation DetailViewController

- (id)initWithNibName:(NSString *)nibNameOrNil bundle:(NSBundle *)nibBundleOrNil
{
    self = [super initWithNibName:nibNameOrNil bundle:nibBundleOrNil];
    if (self) {
        // Custom initialization
    }
    return self;
}

-(void)setLink:(NSString *)link
{
    if (_link != link) {
        _link = link;
        
        [self configureView];
    }
}

-(void)setTitleNews:(NSString *)titleNews
{
    if (_titleNews != titleNews) {
        _titleNews = titleNews;
    }
}

-(void)setDescriptionsNews:(NSString *)descriptionsNews
{
    if (_descriptionsNews != descriptionsNews) {
        _descriptionsNews = descriptionsNews;
    }
}

-(void)configureView
{
    if (self.link) {
        NSURL *url = [NSURL URLWithString:[NSString stringWithString:self.link]];
        NSURLRequest *request = [NSURLRequest requestWithURL:url];
        [self.webView setScalesPageToFit:YES];
        
        [self.webView loadRequest:request];
    }
}

- (void)viewDidLoad
{
    [super viewDidLoad];
    // Do any additional setup after loading the view.
    [self.webView setDelegate:self];
    
    [self configureView];
}

- (void)didReceiveMemoryWarning
{
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

